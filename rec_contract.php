    document.addEventListener('DOMContentLoaded', function () {
  const tabs = document.querySelectorAll('.recruitment-tab');

  // Loop through tabs and add click event listeners
  tabs.forEach(tab => {
    tab.addEventListener('click', function () {
      // Remove 'active' class from all tabs
      tabs.forEach(t => t.classList.remove('active'));

      // Add 'active' class to the clicked tab
      this.classList.add('active');

      // Get the data-tab attribute value
      const tabId = this.dataset.tab;

      // Try to find the corresponding content section
      const contentElement = document.getElementById(tabId + 'Content');

      // Check if content element exists
      if (contentElement) {
        showContent(contentElement);
      } else {
        console.warn(`Content not found for tab: ${tabId}`);
      }
    });
  });

  // Function to show content
  function showContent(contentElement) {
    // Hide all content sections
    const allContents = document.querySelectorAll('.recruitment-content');
    allContents.forEach(c => c.style.display = 'none');

    // Show the selected content section
    contentElement.style.display = 'block';
  }

  // Initially show faculty content by default
  const facultyContent = document.getElementById('facultyContent');
  showContent(facultyContent);
});
</script>
