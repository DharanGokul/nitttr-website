    function aggregateData($data) {
      $aggregatedData = array();
      foreach ($data as $row) {
        $year = $row['YEAR'];
        $participantCount = $row['PARTICIPANT COUNT'];
        if (array_key_exists($year, $aggregatedData)) {
          $aggregatedData[$year] += $participantCount;
        } else {
          $aggregatedData[$year] = $participantCount;
        }
      }
      return $aggregatedData;
